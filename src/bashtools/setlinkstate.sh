if [ "x$1" != "xup" ] && [ "x$1" != "xdown" ];then
	echo "useage: $0 up/down"
	exit 0
fi

for inf in `ip addr | grep ^.: | sed '1,1d' | awk '{printf $2}' | sed "s/:/ /g"`
do
	ip link set $inf $1
done

